export type PetTone = 'gentle' | 'warm' | 'cheer' | 'poetry'

export interface PetMessage {
  text: string
  tone: PetTone
}

function withTone(texts: string[], tone: PetTone): PetMessage[] {
  return texts.map(text => ({ text, tone }))
}


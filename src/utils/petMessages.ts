function getDaypartMessages(date: Date) {
  const hour = date.getHours()

  if (hour >= 5 && hour < 11) {
    return MORNING_MESSAGES
  }

  if (hour >= 11 && hour < 17) {
    return AFTERNOON_MESSAGES
  }

  if (hour >= 17 && hour < 23) {
    return EVENING_MESSAGES
  }

  return NIGHT_MESSAGES
}

export function getPetMessagePool(date: Date, isWelcomeBack: boolean, isOnStreak: boolean) {
  if (isWelcomeBack) {
    return [...getDaypartMessages(date), ...WELCOME_MESSAGES]
  }

  if (isOnStreak) {
    return [...STREAK_MESSAGES, ...EVERYDAY_MESSAGES]
  }

  return [...getDaypartMessages(date), ...EVERYDAY_MESSAGES]
}

export function getPetPoetryMessagePool() {
  return POETRY_MESSAGES
}
